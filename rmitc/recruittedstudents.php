<script>
document.addEventListener("DOMContentLoaded", function () {
  document.querySelectorAll(".student-img img").forEach(function (img) {

    function applyFallback() {
      if (img.dataset.fallbackApplied) return; // prevent loop
      img.dataset.fallbackApplied = "1";
      img.src = "/rmitc/images/students/no-photo.png";
      img.alt = "No Photo Available";
    }

    img.addEventListener("error", applyFallback);

    img.addEventListener("load", function () {
      const w = img.naturalWidth;
      const h = img.naturalHeight;

      // InfinityFree fake-image detection
      if (
        !w || !h ||                 // zero size
        w < 50 || h < 50 ||         // tiny HTML error page
        w > 3000 || h > 3000        // huge HTML page rendered as image
      ) {
        applyFallback();
      }
    });

    // Safety: run once after 2s in case neither fires properly
    setTimeout(function () {
      const w = img.naturalWidth;
      const h = img.naturalHeight;
      if (!w || !h || w < 50 || h < 50) {
        applyFallback();
      }
    }, 2000);

  });
});
</script>
